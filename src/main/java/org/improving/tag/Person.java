package org.improving.tag;

public class Person {
    private String name;
    private String address;
    private String city;
    private String state;
    private String zip;
    private String DOB;



    public String getName(){
        return name;
    }
    public String getAddress(){
        return address;
    }
    public String getCity(){
        return city;
    }
    public String getState(){
        return state;
    }
    public String getZip(){
        return zip;
    }
    public String getDOB(){
        return DOB;
    }
}
